// Variables in TypeScript
let userName: string = "Abhijeet";
let userAge: number = 30;
let isAdmin: boolean = true;

// Using 'let' allows us to reassign values
userName = "John";
userAge = 25;
isAdmin = false;   

// Using 'const' for variables that won't be reassigned
const pi: number = 3.14;
// pi = 3.14159; // This will cause an error because 'pi' is a constant 

